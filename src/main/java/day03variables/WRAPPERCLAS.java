package day03variables;

public class WRAPPERCLAS {


    public static void main(String[] args) {







        //Java, primitive data typelarina bazi faydali methodlar ekleyerek yeni bir veri yapisi olusturmustur.
        //Bu yeni yapi "Wrapper Class" olarak adlandirilir

        // primitive    : char      - boolean - byte -short - int     - long - float - double
        //Wrapper Class : Character - Boolean - Byte -Short - Integer - Long - Float - Double

        int n = 12;
        Integer m = 12;

        //Ornek : primitive int i wrapper Integer a ceviriniz (==>Autoboxing)
        int num = 13;
        Integer wrapperNum = num;

        //Ornek : Wrapper Byte i primitive byte a ceviriniz (==>Unboxing)
        Byte k = 13;
        byte primitveK = k;

     // ornek        :Wrapper Byte i primitive byte a ceviriniz (Unboxing)
       // Byte k = 19;
        byte primitiveK = k;








    }


}




